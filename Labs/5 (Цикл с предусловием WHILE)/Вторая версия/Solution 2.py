# Натуральное число из n цифр является числом Армстронга, если
# сумма его цифр, возведенных в n-ю степень, равна самому числу (как,
# например, 153= 13 + 53 + 33). Дано натуральное N. Выяснить, является ли оно
# числом Армстронга

N = input("N:")

sum = 0
l = len(N)
_l = 0

while _l < l:
    sum += int(N[_l]) ** l
    _l += 1

if sum == int(N):
    print(True)
else:
    print(False)